package PersonalData;

public class Glober {
	
	private String name;
	private String lastName;
	private int age;
	private String location;
	private int catOption;
	
	public Glober(String name, String lastName, int age, String location, int catOption) {
		this.name = name;
		this.lastName = lastName;
		this.age = age;
		this.location = location;
	}
	
	public String getName() {
		return name;
	}
	
	public String getLastName() {
		return lastName;
	}
	
	public int getage() {
		return age;
	}
	
	public String getLocation() {
		return location;
	}
	
	public int getcatOption() {
		return catOption;
	}
	
	
	

}
