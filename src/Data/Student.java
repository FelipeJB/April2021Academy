package Data;

public class Student {
	protected String studentName;
	protected int idStudent;
	protected int studentAge;
	/**
	 * @param studentName
	 * @param idStudent
	 * @param studentAge
	 */
	protected Student(String studentName, int idStudent, int studentAge) {
		super();
		this.studentName = studentName;
		this.idStudent = idStudent;
		this.studentAge = studentAge;
	}

	

}
